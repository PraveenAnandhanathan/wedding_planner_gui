    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String value1 = name.getText();
        double value2 = Double.parseDouble(pricingsstd.getText());
        double value3 = Double.parseDouble(pricingspro.getText());
        double value4 = Double.parseDouble(hours.getText());
        if(std.isSelected()){
            double valuex = value2*value4;
            String result = String.format("%.0f", valuex);
            totalamount.setText(result);
            bill.setText(
                    bill.getText()+"Vendor         --        Pricing   --   Hours   --   Total\n"+
                                    value1+"   --   "+value2+"   --   "+value4+"   --   "+result
            );
        }
        if(pro.isSelected()){
            double valuex = value3*value4;
            String result = String.format("%.0f", valuex);
            totalamount.setText(result);
            bill.setText(
                    bill.getText()+"Vendor         --        Pricing   --   Hours   --   Total\n"+
                                    value1+"   --   "+value3+"   --   "+value4+"   --   "+result
            );
        }
         try{
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/wedding_java", "root", "");
                String sql = "INSERT INTO vendor_count (name, username, amount)"+"values(?,?,?)";
                PreparedStatement pst = con.prepareStatement(sql);
                pst.setString(1, name.getText());
                pst.setString(2, username.getText());
                pst.setString(3, totalamount.getText());
 
                pst.execute();                
                con.close();
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,e);
            }
    }//GEN-LAST:event_jButton2ActionPerformed

